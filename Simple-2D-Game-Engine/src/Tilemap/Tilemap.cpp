Tilemap::Tilemap(SDL_Renderer* renderer) {
	dirt = TextureManager::LoadTexture(renderer, "assets/images/dirt.png");
	water = TextureManager::LoadTexture(renderer, "assets/images/water.png");
	grass = TextureManager::LoadTexture(renderer, "assets/images/grass.png");

	LoadMap(renderer, lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;
	dest.x = dest.y = 0;
}

void Tilemap::LoadMap(SDL_Renderer* renderer, int level[ROWS][COLS]) {
	for (int i = 0; i < ROWS; i++) {
		for (int j = 0; j < COLS; j++) {
			map[i][j] = level[i][j];
		}
	}
}

void Tilemap::DrawMap(SDL_Renderer* renderer) {

	int type = 0;
	for (int i = 0; i < ROWS; i++) {
		for (int j = 0; j < COLS; j++) {
			type = map[i][j];
			dest.x = j * dest.w;
			dest.y = i * dest.h;
			switch (type){
			case 0 :
				TextureManager::Draw(renderer, water, src, dest);
				break;
			case 1:
				TextureManager::Draw(renderer, grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(renderer, dirt, src, dest);
				break;
			}
		}
	}
}